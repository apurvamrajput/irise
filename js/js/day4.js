//number(int/float)
let x = 10.5
console.log(x,typeof x);

//string
let y = "hi"
console.log(y,typeof y);

//bool(true/false)
let z = true
console.log(z,typeof z);

//undefine
let a;
console.log(a,typeof a);

//null
let b = null;
console.log(b,typeof b);


//BigInt
let c = 1234567891234567894567n
console.log(c,typeof c);

//symbol
let age = Symbol(23);
console.log(age,typeof age);



